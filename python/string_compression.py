def string_compression(string):
    i=0
    j=i+1
    
    while j<len(string):
        count=1
        while j<len(string) and string[i]==string[j]:
            j+=1
            count+=1
    
        if count>1:
            count=list(str(count))
            len_count=len(count)
            string[i+1:i+1+len_count]=count
            
            for k in range(i+1+len_count,j):
                string.pop(i+1+len_count)
            
            i=i+len_count
            j=i+1
        elif count==1:
            i+=1
            j=i+1
    
    return string

